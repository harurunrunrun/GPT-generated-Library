    void init(int n_) {
        n = n_;
        bits.assign((n + 63) >> 6, 0ULL);
        pref.clear();
        built = false;
    }

    /*
     * size()
     * ------
     * ビット列の長さを返す。
     *
     * 時間計算量:
     *   O(1)
     */
